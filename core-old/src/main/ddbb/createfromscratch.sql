            LENGTH(CC.TAS_TRANSA)+NVL(LENGTH ( STATUS ),0) + NVL (LENGTH(CC.TAS_MISSAT),0) + 10 < 2048 THEN
            STATUS := STATUS || CC.TAS_TRANSA||' Error: '||CC.TAS_MISSAT||CHR(13)||CHR(10);
         END IF;
      END LOOP;
      RETURN STATUS;
   END;
   FUNCTION SC_MAQUIN (CODI IN VARCHAR2) RETURN VARCHAR2 
   IS
    CURSOR C IS 
      SELECT TAS_TRANSA, TAS_STATUS, TAS_MISSAT
      FROM   SC_TASQUE
      WHERE  TAS_MAQUIN = CODI;
      STATUS VARCHAR2(2048) := NULL;
   CC C%ROWTYPE;
   BEGIN
 
      FOR CC IN C LOOP
         IF CC.TAS_STATUS = 'P' OR CC.TAS_STATUS IS NULL AND 
            LENGTH(CC.TAS_TRANSA)+NVL(LENGTH ( STATUS ),0) + 10 < 2048 THEN
            STATUS := STATUS || CC.TAS_TRANSA||' Pendent'||CHR(13)||CHR(10);
         ELSIF CC.TAS_STATUS = 'E' AND 
            LENGTH(CC.TAS_TRANSA)+NVL(LENGTH ( STATUS ),0) + NVL (LENGTH(CC.TAS_MISSAT),0) + 10 < 2048 THEN
            STATUS := STATUS || CC.TAS_TRANSA||' Error: '||CC.TAS_MISSAT||CHR(13)||CHR(10);
         END IF;
      END LOOP;
      RETURN STATUS;
   END;
