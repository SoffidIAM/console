package es.caib.seycon.ng.web.utils;

public class TagValueEntry {
	String tag;
	String value;
	public String getTag() {
		return tag;
	}
	public void setTag(String tag) {
		this.tag = tag;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
}
